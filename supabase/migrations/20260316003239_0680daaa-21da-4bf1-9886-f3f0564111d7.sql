
-- Add CIN to clients table
ALTER TABLE public.clients ADD COLUMN IF NOT EXISTS cin TEXT;

-- Create courts reference table
CREATE TABLE IF NOT EXISTS public.courts (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  court_type TEXT NOT NULL DEFAULT 'ابتدائية',
  city TEXT NOT NULL,
  address TEXT,
  addressee TEXT NOT NULL DEFAULT 'السيد رئيس المحكمة',
  created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.courts ENABLE ROW LEVEL SECURITY;

-- Anyone can read courts
CREATE POLICY "Anyone can read courts" ON public.courts FOR SELECT TO public USING (true);

-- Only directors can manage courts
CREATE POLICY "Directors can manage courts" ON public.courts FOR ALL TO authenticated
  USING (has_role(auth.uid(), 'director'::app_role))
  WITH CHECK (has_role(auth.uid(), 'director'::app_role));

